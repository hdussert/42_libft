int	ft_pow(int x, int y)
{
	int res;

	if (y < 0)
		return (0);
	res = 1;
	if (x == 1)
		return (1);
	if (y == 0)
		return (1);
	if (y == 1)
		return (x);
	res *= ft_pow(x, y - 1);
	return (res);
}
