#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int i;
	int j;

	i = -1;
	j = -1;
	while (s1[++i])
		;
	while (s2[++j] && (size_t)j < n)
		s1[i + j] = s2[j];
	s1[i + j] = '\0';
	return (s1);
}
